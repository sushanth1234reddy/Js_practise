// 1. Write a program to determine the type of coffee based on the user's selection:
//     - 1: Espresso
//     - 2: Cappuccino
//     - 3: Latte
//     - 4: Mocha
let coffee="Cappuccino";
switch(coffee){
    case "Expresso":
        console.log("User likes Expresso");
        break;
    case "Cappuccino":
        console.log("User likes Cappuccino");
        break;
    case "Latte":
        console.log("User likes Latte");
        break;
    case "Mocha":
        console.log("User likes Mocha");
        break;
    default:
        console.log("User does not like coffee");    
}
